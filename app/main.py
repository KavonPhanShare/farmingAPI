def generate_farming_alerts(stats: dict):
    alerts = []
    
    # 1. Disease Risk (Fungal/Blight)
    if stats['humidity'] > 80 and stats['temperature'] > 20:
        alerts.append({
            "type": "DISEASE_RISK",
            "level": "HIGH",
            "message": "High humidity and warmth detected. Monitor for fungal growth or blight."
        })

    # 2. Operations (Chemical Spraying)
    if stats['wind_speed'] > 15:
        alerts.append({
            "type": "OPERATIONAL_DANGER",
            "level": "CRITICAL",
            "message": "Wind speeds too high for pesticide/fertilizer spraying. Risk of drift."
        })
    elif stats['wind_speed'] < 5:
        alerts.append({
            "type": "OPERATIONAL_INFO",
            "level": "IDEAL",
            "message": "Perfect wind conditions for precise spraying."
        })

    # 3. Resource Management (Watering)
    if stats['precipitation'] > 0:
        alerts.append({
            "type": "RESOURCE_SAVING",
            "level": "INFO",
            "message": "Rain detected. Suspend irrigation to save water and prevent root rot."
        })

    # 4. Planting Safety (Soil Temp)
    if stats['soil_temp'] < 10:
        alerts.append({
            "type": "PLANTING_WARNING",
            "level": "WARNING",
            "message": "Soil is cold. Heat-loving seeds (maize/peppers) may not germinate."
        })

    return alerts

CROP_FACTS = {
    "tomato": {"min_temp": 10, "max_temp": 35, "opt_low": 21, "opt_high": 24, "water": "high"},
    "maize": {"min_temp": 10, "max_temp": 35, "opt_low": 25, "opt_high": 30, "water": "medium"},
    "lettuce": {"min_temp": 7, "max_temp": 24, "opt_low": 15, "opt_high": 18, "water": "high"}
}

def analyze_crop_suitability(current_temp, current_humidity):
    report = {}
    
    for crop, facts in CROP_FACTS.items():
        # Check if current temp is in the 'Danger Zone'
        if current_temp < facts["min_temp"] or current_temp > facts["max_temp"]:
            status = "CRITICAL: Extreme conditions for this crop."
        # Check if current temp is in the 'Sweet Spot'
        elif facts["opt_low"] <= current_temp <= facts["opt_high"]:
            status = "OPTIMAL: Perfect growing conditions."
        else:
            status = "STRESSED: Sub-optimal growth likely."
            
        report[crop] = status
        
    return report

def check_disease_risk(temp, humidity):
    risk_level = "Low"
    potential_disease = "None"
    
    # Famous "Smith Period" logic for Blight
    if humidity > 90 and 15 < temp < 25:
        risk_level = "High"
        potential_disease = "Late Blight / Fungal Outbreak"
    elif humidity > 80 and temp > 28:
        risk_level = "Medium"
        potential_disease = "Root Rot / Bacterial Wilt"
        
    return {"risk": risk_level, "target": potential_disease}

