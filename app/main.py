@app.get("/users")
def get_users():
    """Reads all rows from the users table."""
    with SessionLocal() as db:
        # 1. Query the reflected class
        all_users = db.query(Users).all()
        
        # 2. Convert SQLAlchemy objects to a list of dicts for JSON response
        # Using .__dict__ is the 'hackathon speed' way to serialize
        return [
            {column: getattr(user, column) for column in user.__table__.columns.keys()} 
            for user in all_users
        ]

@app.post("/users/add")
def create_user(username: str, email: str):
    """Writes a new row to the users table."""
    with SessionLocal() as db:
        # 1. Create a new instance of the reflected class
        new_user = Users(username=username, email=email)
        
        # 2. Add and Commit
        db.add(new_user)
        db.commit()
        db.refresh(new_user) # Get the new ID generated by Postgres
        
        return {"status": "success", "user_id": new_user.id}

@app.get("/weather", tags=["external"])
async def get_weather(lat: float, lon: float):
    url = "https://api.open-meteo.com/v1/forecast"
    params = {
        "latitude": lat,
        "longitude": lon,
        "current": "temperature_2m,relative_humidity_2m,precipitation,wind_speed_10m,soil_temperature_0cm",
        "timezone": "auto"
    }
    
    async with httpx.AsyncClient() as client:
        try:
            response = await client.get(url, params=params)
            response.raise_for_status()
            data = response.json()
            current = data.get("current")
            units = data.get("current_units")

            return {
                "location": {"lat": lat, "lon": lon},
                "stats": {
                    "temperature": current.get("temperature_2m"),
                    "humidity": current.get("relative_humidity_2m"),
                    "precipitation": current.get("precipitation"),
                    "wind_speed": current.get("wind_speed_10m"),
                    "soil_temp": current.get("soil_temperature_0cm")
                },
                "units": {
                    "temp": units.get("temperature_2m"),
                    "humidity": units.get("relative_humidity_2m"),
                    "precipitation": units.get("precipitation")
                }
            }
        except httpx.HTTPError as e:
            raise HTTPException(status_code=500, detail=f"Weather service error: {str(e)}")

